    private void JB_AddBalanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_AddBalanceActionPerformed
        // TODO add your handling code here:
        try{ 
            double AddBalance = Double.parseDouble(JT_AddBalance.getText());
            double balance = this.balance;
            String username = this.username;
            List<String> fileContent = new ArrayList<>();

            if (AddBalance == 0){
                JOptionPane.showMessageDialog(this, "Insert the amount");
            }else {
                JT_AddBalance.setVisible(false);
                JB_AddBalance.setVisible(false);
                JL_AddBalance.setVisible(false);
                balance = balance + AddBalance;

                JL_Balance.setText("Current Balance:  " + balance);

                String filePath = Config.getFilePath("UserListPath");
                File file = new File(filePath);

                try {
                    BufferedReader br = new BufferedReader(new FileReader(file));
                    Object[] tableLines = br.lines().toArray();

                    for(int i=0 ; i < tableLines.length ; i++){
                        String line = tableLines[i].toString().trim();
                        String[] dataRow = line.split("/");
                        if (dataRow[0].equals("Customer") && dataRow[1].equals(username)){
                            dataRow[5] = String.valueOf(balance);
                            line = String.join("/", dataRow);
                        }
                        fileContent.add(line);
                        JL_Balance.setText(username +"'s Balance:  " + balance);
                    }
                } catch (FileNotFoundException ex) {
                    JOptionPane.showMessageDialog(this,"The Password doesn't match");
                }
                try (BufferedWriter bw = new BufferedWriter(new FileWriter(file))) {
                    for (String updatedLine : fileContent) {
                        bw.write(updatedLine);
                        bw.newLine();
                    }
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, "Error writing to the file: " + ex.getMessage());
                }
            }
        }catch (NumberFormatException ex) {
        // This block executes if parsing fails
        JOptionPane.showMessageDialog(this, "Please enter a valid number");
        }

        
        
    }//GEN-LAST:event_JB_AddBalanceActionPerformed

