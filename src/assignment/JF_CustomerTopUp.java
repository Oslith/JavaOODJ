    private void JB_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_SearchActionPerformed
        // TODO add your handling code here:
        String Username = JT_Username.getText();
                
        String filePath = Config.getFilePath("UserListPath");
        File file = new File(filePath);
        
        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            Object[] tableLines = br.lines().toArray();
            boolean isUser = false;
            String balance = "";
            for(int i=0 ; i < tableLines.length ; i++){
                String line = tableLines[i].toString().trim();
                String[] dataRow = line.split("/");
                if (dataRow[0].equals("Customer") && dataRow[1].equals(Username)){
                    balance = dataRow[5];
                    this.username = Username;
                    this.balance = Double.parseDouble(balance);
                    isUser = true;
                    JL_Balance.setText("Current Balance:  " + balance);
                    
                }else{
                    continue;
                    
                }

            }
            if (!isUser){
                    JL_Balance.setText("Customer not found");
                    this.username = "";
                    JT_AddBalance.setVisible(false);
                    JB_AddBalance.setVisible(false);
                    JL_AddBalance.setVisible(false);
            }else{
                    JT_AddBalance.setVisible(true);
                    JB_AddBalance.setVisible(true);
                    JL_AddBalance.setVisible(true);
            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"The Password doesn't match");
        }
    }//GEN-LAST:event_JB_SearchActionPerformed

