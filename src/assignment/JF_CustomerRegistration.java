    private void JBDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel VendorTable = (DefaultTableModel) JTVendor.getModel();
        String Username = JFUsername.getText();

        if (JTVendor.getSelectedRowCount() == 1) {
            String filePath = Config.getFilePath("UserListPath");
            File file = new File(filePath);
            List<String> lines = new ArrayList<>();
            String lineToRemove = "";

            try {
                BufferedReader br = new BufferedReader(new FileReader(file));
                String line;
                while ((line = br.readLine()) != null) {
                    lines.add(line);
                    String[] dataRow = line.split("/");
                    if ("Vendor".equals(dataRow[0]) && dataRow[1].equals(Username)) {
                        lineToRemove = line;
                    }
                }
                br.close();

                // Remove the identified line
                if (!lineToRemove.isEmpty()) {
                    lines.remove(lineToRemove);
                    // Write the remaining lines back to the file
                    BufferedWriter bw = new BufferedWriter(new FileWriter(file));
                    for (String l : lines) {
                        bw.write(l + System.lineSeparator());
                    }
                    bw.close();
                    // Remove row from the table
                    VendorTable.removeRow(JTVendor.getSelectedRow());
                }

            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(this, "File not found.");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error reading or writing file.");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Haven't selected any table.");
        }
    }//GEN-LAST:event_JBDeleteActionPerformed

    private void JBEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBEditActionPerformed
        // TODO add your handling code here:
        DefaultTableModel VendorTable = (DefaultTableModel) JTVendor.getModel();
        if (JTVendor.getSelectedRowCount() == 1) {
        String Username = JFUsername.getText();
        String Password = JFPassword.getText();
        String Name = JFName.getText();
        String PhoneNumber = JFPhoneNumber.getText();
        String StoreName = JFStoreName.getText();
        String Location = JFLocation.getText();
        String Balance = JFBalance.getText();
        int selectedRow = JTVendor.getSelectedRow();

        VendorTable.setValueAt(Username, selectedRow, 0);
        VendorTable.setValueAt(Password, selectedRow, 1);
        VendorTable.setValueAt(Name, selectedRow, 2);
        VendorTable.setValueAt(PhoneNumber, selectedRow, 3);
        VendorTable.setValueAt(StoreName, selectedRow, 4);
        VendorTable.setValueAt(Location, selectedRow, 5);
        VendorTable.setValueAt(Balance, selectedRow, 6);

        String filePath = Config.getFilePath("UserListPath");
        File file = new File(filePath);

        try {
            BufferedReader br = new BufferedReader(new FileReader(file));
            List<String> lines = new ArrayList<>();
            String line;

            // Read all lines from the file
            while ((line = br.readLine()) != null) {
                lines.add(line);
            }

            br.close();

            // Find the correct line based on the username
            for (int i = 0; i < lines.size(); i++) {
                String[] data = lines.get(i).split("/");
                if (data.length > 1 && data[1].equals(VendorUsername)) {
                    // Update the selected row data in the list
                    String userType = data[0];
                    String updatedLine = userType + "/" +
                            Username + "/" +
                            Password + "/" +
                            Name + "/" +
                            PhoneNumber + "/" +
                            StoreName + "/" +
                            Location + "/" +
                            Balance;

                    lines.set(i, updatedLine);

                    // Write the updated list back to the file
                    BufferedWriter bw = new BufferedWriter(new FileWriter(file));
                    for (String updated : lines) {
                        bw.write(updated + "\n");
                    }

                    bw.close();

                    clearTextField();
                    
                    JFPassword2.setText("");
                    JFName.setText("");
                  
                    JOptionPane.showMessageDialog(this, "Successfully Updated!");
                    return;
                }
            }

            JOptionPane.showMessageDialog(this, "Username not found in the text file");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error updating text file: " + ex.getMessage());
        }
    } else {
        if (JTVendor.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Table is empty");
        } else {
            JOptionPane.showMessageDialog(this, "Haven't selected any table row");
        }
    }
    }//GEN-LAST:event_JBEditActionPerformed

   
