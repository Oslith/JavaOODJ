   private boolean CheckDup(String Username,String StoreName,String Location,String PhoneNumber) {
    DefaultTableModel VendorTable = (DefaultTableModel) JTVendor.getModel();
    for (int i = 0; i < VendorTable.getRowCount(); i++) {
        String existingUsername = (String) VendorTable.getValueAt(i, 0);
        String existingStoreName = (String) VendorTable.getValueAt(i, 4);
        String existingLocation = (String) VendorTable.getValueAt(i, 5);
        String existingPhoneNumber = (String) VendorTable.getValueAt(i, 3);
        if (Username.equals(existingUsername)) {
            JOptionPane.showMessageDialog(this, "Username already exists. Please use a different username.");
            return true;
        } else if (StoreName.equals(existingStoreName)){
            JOptionPane.showMessageDialog(this, "StoreName already exists. Please use a different StoreName.");
            return true;
        }else if (Location.equals(existingLocation)){
            JOptionPane.showMessageDialog(this, "Location already exists. Please use a different Location.");
            return true;
        }else if (PhoneNumber.equals(existingPhoneNumber)){
            JOptionPane.showMessageDialog(this, "PhoneNumber already exists. Please use a different PhoneNumber.");
            return true;
        }
    }
    return false;
}
